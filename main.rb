
# Lowercase to Uppercase ---
puts "Lowercase to Uppercase:"
string1 = "hello everyone"
puts "String: '#{string1}'"
puts string1.upcase

puts "\n" # separation ---

# Capitalize a sentence ---
puts "Capitalize a Sentence:"
string1 = "hello everyone"
puts "String: '#{string1}'"
puts string1.capitalize

puts "\n" # separation ---

# Swap the case over ---
puts "Swap the case over:"
string2 = "hello EVERYONE"
puts "String: '#{string2}'"
puts string2.swapcase

puts "\n" # separation ---

# Reverse the text ---
puts "Reverse the Text"
string2 = "hello everyone"
puts "String: '#{string2}'"
puts string2.reverse